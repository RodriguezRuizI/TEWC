            <div>
                <h2>Prótesis</h2>
                <hr>
                <h4>¿Puedo hacer deporte con mi prótesis?</h4>
                <p>La respuesta a esta pregunta depende de tres factores:
                    <br><br>Las características físicas que tenga el paciente, es decir, su grado de movilidad, edad, etc.
                    <br><br>Las características tecnológicas de la prótesis. Hay prótesis de alta generación que permiten hacer incluso atletismo.
                    <br><br>Si el paciente practicaba ya ese deporte antes de sufrir la pérdida de un miembro. Lógicamente es más fácil retomar el deporte que se hacía con anterioridad que practicarlo por primera vez.
                    <br><br>Por ejemplo, un esquiador que sufra una amputación por debajo de la rodilla, va a conservar esta articulación, vital para la práctica del esquí, con lo cual no va a tener gran problema para retomar su afición porque no pierde la movilidad esencial y porque ya domina la técnica.</p>
                <h4>¿Me la puedo colocar yo mism@?</h4>
                <p>Normalmente sí, siempre y cuando tenga el grado de independencia y movilidad para poder manejarla.</p>
            </div>
