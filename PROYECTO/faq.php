                <hr>
                <h4>¿Puedo hacer deporte con mi prótesis?</h4>
                <p>Entre otras cosas se analizan cómo se reparten las presiones y las cargas sobre las distintas partes del pie (talón, metatarsos etc.) en las diferentes fases de la pisada (apoyo, apoyo medio y propulsión) así como el eje de la pisada.</p>
                <h4>¿Qué aporta un estudio de la marcha?</h4>
                <p>El estudio de la marcha aporta una valiosa información, que no es posible observar en una pedigrafía realizada en bipedestación. No se pisa igual cuando se está parado que cuando se anda. Las diferentes maneras de pisar en una u otra circunstancia pueden dar lugar a problemas diferentes y soluciones adicionales que han de integrarse en la plantilla.</p>
