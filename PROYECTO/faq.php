            <div>
                <h2>Fajas</h2>
                <hr>
                <h4> ¿Tengo que dormir con la faja?</h4>
                <p>La circulación sanguínea se ralentiza por la noche. Si a eso le añadimos el hecho de que todas las prendas elásticas afectan a la circulación, fajas incluidas, la conclusión es que la faja ha de quitarse antes de irse a dormir.
                    <br><br>Existe alguna excepción a esta regla: si el paciente necesita una inmovilización absoluta, en caso de una fractura de columna etc. la faja debe ser usada también por la noche, aunque  en estos supuestos no se suelen indicar fajas sino corsés de termoplástico. En cualquier caso han de seguirse siempre las prescripciones del especialista.</p>
                <h4>¿Qué ropa puedo ponerme con la faja?</h4>
                <p>En principio puede ponerse cualquier tipo de ropa sin más límites que los estéticos, por lo que recomendamos que no se ponga ropa muy ajustada o transparente por encima.</p>
