                <h2>Plantillas:</h2>
                <hr>
                <h4>¿Tengo que llevar las plantillas siempre?</h4>
                <p>Depende de la patología subyacente: si la plantilla se prescribe para un proceso no crónico como por ejemplo, para acelerar la recuperación del pie tras una intervención quirúrgica, sólo deberá usarse hasta que se complete dicha recuperación.<br>
                    Sin embargo, si la patología subyacente tiene un carácter crónico como por ejemplo, un pie cavo severo o una fascitis plantar cronificada, en ese caso el uso de la plantilla habrá de ser permanente, pues se perderían las ventajas obtenidas al dejar de usarla. En cualquier caso consulte con su médico.</p>
                <h4>¿Para ponerme plantillas debo cambiar de calzado?</h4>
                <p>No necesariamente.  Con los nuevos materiales disponibles hoy en día, las plantillas pueden hacerse muy finas y adaptarse casi a cualquier tipo de calzado, salvo algunas excepciones como sandalias abiertas etc. En cualquier caso esta circunstancia ha de valorarse caso por caso con el paciente.</p>
                <h4>Cuando me pongo plantillas, ¿debo llevar un número más de zapato para que no me apriete?</h4>
                <p>Lo aconsejable es que lleve la talla de zapato de siempre pero procurando que sea un tipo de zapato con una horma lo suficientemente amplia para alojar el pie y la plantilla.</p>
            </div>
